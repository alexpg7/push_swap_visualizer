#include "libft.h"

int	ft_isalnum(int c)
{
	int	num;
	int	up;
	int	low;

	num = (c >= '0' && c <= '9');
	up = (c >= 'A' && c <= 'Z');
	low = (c >= 'a' && c <= 'z');
	if (num || up || low)
		return (1);
	else
		return (0);
}
